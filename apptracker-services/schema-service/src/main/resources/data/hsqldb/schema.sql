CREATE TABLE CANDIDATEINFO (
  AID BIGINT  NOT NULL,
  LASTNAME VARCHAR(100) NOT NULL,
    FIRSTNAME VARCHAR(100) NOT NULL,
        MIDINITIAL  VARCHAR(3),
    PREFFIRSTNAME VARCHAR(100) NOT NULL,
    EMAIL VARCHAR(100) NOT NULL,
    CONTACTPHONE VARCHAR(13) NOT NULL,
    CURSTATUS INT NOT NULL,

  PRIMARY KEY(AID)
);

